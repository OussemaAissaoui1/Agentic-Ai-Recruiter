# Batch test (multiple scenarios)
# ---------------------------------------------------------------------------

async def run_batch(agent: NLPAgent):
    """
    Run a batch of test scenarios to verify agent behavior.

    Tests various answer types:
    - Opening (first turn)
    - Very short answer
    - Detailed answer
    - Skip request
    - Nonsense answer
    """

    session_id = "test-batch"
    job_role = "AI Engineering Intern"
    history: list[tuple[str, str]] = []

    test_scenarios = [
        ("hello im ready for the interview", "Opening turn — should greet"),
        ("I worked on GraphRAG", "Very short — should follow up"),
        ("yes", "Extremely short — should follow up again"),
        (
            "I built a knowledge graph from enterprise documents using Neo4j, "
            "then applied LLM reasoning with a fine-tuned model to detect "
            "anomalous patterns in transaction data. We achieved 94% precision "
            "on the validation set and reduced false positives by 40%.",
            "Detailed — should move to new topic"
        ),
        ("can we skip this topic", "Skip request — should respect it"),
        ("haha idk", "Nonsense — should redirect gently"),
    ]
    # Each scenario is (candidate_answer, expected_behavior_description).

    print(f"\n{'='*70}")
    print(f"🧪 Batch Test — {len(test_scenarios)} scenarios")
    print(f"{'='*70}\n")

    for i, (answer, description) in enumerate(test_scenarios, 1):
        # enumerate(list, 1) starts counting from 1.

        print(f"--- Scenario {i}/{len(test_scenarios)}: {description} ---")
        print(f"CANDIDATE: {answer[:80]}{'...' if len(answer) > 80 else ''}")
        # Truncate long answers for display.

        request = QuestionGenerationRequest(
            candidate_cv=TEST_CV,
            job_role=job_role,
            conversation_history=history[-6:],
            candidate_latest_answer=answer,
            session_id=session_id,
        )

        response = await agent.generate_question(request)

        indicator = "🔄 FOLLOW-UP" if response.is_follow_up else "➡️  NEW TOPIC"
        print(f"RECRUITER: {response.question}")
        print(f"STATUS:    {indicator} | reason={response.reasoning}")
        print(f"METRICS:   ttft={response.metrics.ttft_sec:.2f}s | latency={response.metrics.latency_sec:.2f}s" if response.metrics.ttft_sec else f"METRICS:   latency={response.metrics.latency_sec:.2f}s")
        print()

        history.append((answer, response.question))
        # Accumulate history so later scenarios see previous turns.

    agent.end_session(session_id)
    print(f"{'='*70}")
    print(f"✅ Batch test complete")
    print(f"{'='*70}\n")


