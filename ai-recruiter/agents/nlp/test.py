# Entry point
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(description="NLP Agent Direct Test")

    parser.add_argument(
        "--single",
        type=str,
        default=None,
        help="Single-shot mode: provide one candidate answer",
    )
    # Example: python test_agent.py --single "I used Python for ML"

    parser.add_argument(
        "--batch",
        action="store_true",
        help="Run batch test scenarios",
    )
    # Example: python test_agent.py --batch

    parser.add_argument(
        "--compact",
        action="store_true",
        help="Use compact system prompt (faster TTFT)",
    )
    # Example: python test_agent.py --compact

    parser.add_argument(
        "--max-model-len",
        type=int,
        default=2048,
        help="Max context length for vLLM (default: 2048)",
    )

    parser.add_argument(
        "--gpu-memory",
        type=float,
        default=0.92,
        help="GPU memory fraction (default: 0.92)",
    )

    args = parser.parse_args()
    # Parse command-line arguments.

    print("\n🔧 Initializing NLP Agent...")
    print("   (This loads the model — takes ~60 seconds)\n")

    # --- Create the agent ---
    agent = NLPAgent(
        use_compact_prompt=args.compact,
        max_model_len=args.max_model_len,
        gpu_memory_utilization=args.gpu_memory,
    )
    # This loads the model into GPU memory.
    # The model_path default is set inside NLPAgent.__init__().

    async def _run():
        """Async wrapper for the test functions."""

        print("🔥 Warming up engine...\n")
        await agent.warmup()
        # Prime KV cache and CUDA graphs.

        if args.single:
            await run_single(agent, args.single)
        elif args.batch:
            await run_batch(agent)
        else:
            await run_chat(agent)
            # Default: interactive chat mode.

    asyncio.run(_run())
    # asyncio.run() creates an event loop and runs the async function.
    # This is the standard way to run async code from synchronous Python.


if __name__ == "__main__":
    main()
    # Only run if this file is executed directly (not imported).