async def run_chat(agent: NLPAgent):
    """
    Run an interactive chat session with the agent.

    You type as the candidate, the agent responds as the recruiter.
    This simulates the full interview flow without any server.
    """

    session_id = "test-session-001"
    # Arbitrary session ID. In production, this is generated by the orchestrator.

    job_role = "AI Engineering Intern"
    # The job role shapes the interview focus.

    history: list[tuple[str, str]] = []
    # Conversation history: [(candidate_answer, recruiter_question), ...]
    # Starts empty — the first turn is the opening greeting.

    print("\n" + "=" * 70)
    print("🎤 NLP Agent — Direct Test Mode")
    print("=" * 70)
    print("You are the candidate. Type your answers.")
    print("Commands: /quit  /restart  /debug  /status")
    print("=" * 70 + "\n")

    while True:
        # --- Get candidate input ---
        try:
            candidate_input = input("Candidate > ").strip()
            # Read input from the terminal.
            # .strip() removes leading/trailing whitespace.
        except (EOFError, KeyboardInterrupt):
            # EOFError: input stream ended (piped input finished).
            # KeyboardInterrupt: user pressed Ctrl+C.
            print("\n\n✅ Interview ended.")
            break

        # --- Handle commands ---
        if not candidate_input:
            print("Please type something.\n")
            continue
            # Skip empty inputs.

        if candidate_input.lower() in {"/quit", "quit", "exit"}:
            print("\n✅ Interview ended. Good luck!")
            break

        if candidate_input.lower() == "/restart":
            agent.reset_session(session_id)
            # Reset the state tracker without destroying the session.
            history = []
            # Clear local history too.
            print("\n🔄 Interview reset. Starting fresh.\n")
            continue

        if candidate_input.lower() == "/debug":
            # Show detailed analysis of the PREVIOUS answer.
            if history:
                last_answer = history[-1][0]
                analysis = agent.analyze_response(session_id, last_answer)
                print(f"\n[DEBUG] Last answer analysis:")
                for key, value in analysis.items():
                    print(f"  {key}: {value}")
                print()
            else:
                print("\n[DEBUG] No previous answer to analyze.\n")
            continue

        if candidate_input.lower() == "/status":
            stats = agent.get_session_stats(session_id)
            print(f"\n[STATUS]")
            for key, value in stats.items():
                print(f"  {key}: {value}")
            print()
            continue

        # --- Build the request ---
        request = QuestionGenerationRequest(
            candidate_cv=TEST_CV,
            # The candidate's CV — used to build the system prompt.

            job_role=job_role,
            # Job title for interview focus.

            conversation_history=history[-6:],
            # Last 6 turns of history.
            # [-6:] = slice the last 6 elements (or all if fewer than 6).

            candidate_latest_answer=candidate_input,
            # What the candidate just said.

            session_id=session_id,
            # The session ID for state tracking.
        )

        # --- Generate the recruiter's question ---
        try:
            response = await agent.generate_question(request)
            # This is the main call. It:
            # 1. Analyzes the answer
            # 2. Decides follow-up vs new topic
            # 3. Runs vLLM inference
            # 4. Validates and cleans the response
        except Exception as e:
            print(f"\n❌ Generation error: {e}\n")
            continue

        # --- Display the response ---
        print(f"\nRecruiter > {response.question}")
        # The actual question to display/speak.

        # Status indicator
        if response.is_follow_up:
            indicator = "🔄 FOLLOW-UP"
        else:
            indicator = "➡️  NEW TOPIC"
        # Visual indicator showing whether we're digging deeper or moving on.

        # Metrics
        ttft_str = f"{response.metrics.ttft_sec:.2f}s" if response.metrics.ttft_sec else "N/A"
        # Format TTFT. Show "N/A" if no tokens were generated.

        print(
            f"[{indicator} | "
            f"reason={response.reasoning} | "
            f"depth={response.topic_depth} | "
            f"turn={response.turn_count} | "
            f"ttft={ttft_str} | "
            f"latency={response.metrics.latency_sec:.2f}s | "
            f"tokens={response.metrics.num_tokens} | "
            f"tok/s={response.metrics.tokens_per_sec:.1f}]"
        )
        print()

        # --- Update history ---
        history.append((candidate_input, response.question))
        # Add this turn to the conversation history.
        # Next time, the model will see this in its context.

    # --- Cleanup ---
    agent.end_session(session_id)
    # Release session resources.


