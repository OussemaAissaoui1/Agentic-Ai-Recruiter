async def run_single(agent: NLPAgent, candidate_answer: str):
    """
    Generate a single response — useful for quick testing.

    Args:
        agent: The NLP agent.
        candidate_answer: A single candidate answer to respond to.
    """

    session_id = "test-single"

    request = QuestionGenerationRequest(
        candidate_cv=TEST_CV,
        job_role="AI Engineering Intern",
        conversation_history=[],
        # No history for single-shot mode.
        candidate_latest_answer=candidate_answer,
        session_id=session_id,
    )

    print(f"\n{'='*70}")
    print(f"CANDIDATE : {candidate_answer}")
    print(f"{'='*70}")

    response = await agent.generate_question(request)
    # Generate one question.

    print(f"RECRUITER : {response.question}")
    print(f"REASONING : {response.reasoning}")
    print(f"FOLLOW-UP : {response.is_follow_up}")
    print(f"TTFT      : {response.metrics.ttft_sec:.3f}s" if response.metrics.ttft_sec else "TTFT      : N/A")
    print(f"LATENCY   : {response.metrics.latency_sec:.3f}s")
    print(f"TOKENS    : {response.metrics.num_tokens}")
    print(f"TOK/SEC   : {response.metrics.tokens_per_sec:.1f}")
    print(f"{'='*70}\n")

    agent.end_session(session_id)


